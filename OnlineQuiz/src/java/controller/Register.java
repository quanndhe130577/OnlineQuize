    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    
    /*
    purpose : get information from jsp to insert database
    author: quanndhe130577
    time : 02/2020
    */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        String res_username = String.valueOf(request.getParameter("res_username"));
        String res_password = String.valueOf(request.getParameter("res_password"));
        String type = String.valueOf(request.getParameter("type"));
        String res_email = String.valueOf(request.getParameter("res_email"));

//        
        AccountDAO Adao = new AccountDAO();
        int max;
        try {
            max = Adao.getIdAccountMax()+1;
            Account a = new Account(max, res_username, res_password, type,res_email );
            String rs = Adao.insertAccount(a);
            //if value return is "success"
            if(rs.equals("Success")) {
                request.getSession().setAttribute("username", a.getUsername());
                response.sendRedirect("Home.jsp");
            }else {
                response.getWriter().write(rs);
            }
        } catch (SQLException ex) {
            Logger.getLogger(Register.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

